package Modelo;

/**
 *
 * @author YURY
 */
public class Mes {
    private long idMes;
    private String nombre;

    /**
     * @return the idMes
     */
    public long getIdMes() {
        return idMes;
    }

    /**
     * @param idMes the idMes to set
     */
    public void setIdMes(long idMes) {
        this.idMes = idMes;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
